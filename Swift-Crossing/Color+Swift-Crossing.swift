import Cocoa

extension NSColor {
    class func treeGreen() -> NSColor {
        return NSColor(calibratedRed: 0, green: 0.75, blue: 0, alpha: 1.0)
    }

    class func grassGreen() -> NSColor {
        return NSColor(calibratedRed: 77.0 / 255.0, green: 189.0 / 255.0, blue: 51 / 255.0, alpha: 1.0)
    }
}